package com.xworkz.encapsulation.app;

public class Ola {
	    private String passengerName;
	    private String driverName;
	    private String pickupLocation;
	    private String dropLocation;
	    private String carModel;
	    private String carNumber;
	    private String driverContact;
	    private double rideDistance;
	    private double rideDuration;
	    private double rideCost;
	    private boolean isRideCompleted;
	    private boolean isDriverRated;
	    private boolean isPaymentCompleted;
	    private int numberOfPassengers;

	    public String getPassengerName() {
	        return passengerName;
	    }

	    public void setPassengerName(String passengerName) {
	        this.passengerName = passengerName;
	    }

	    public String getDriverName() {
	        return driverName;
	    }

	    public void setDriverName(String driverName) {
	        this.driverName = driverName;
	    }

	    public String getPickupLocation() {
	        return pickupLocation;
	    }

	    public void setPickupLocation(String pickupLocation) {
	        this.pickupLocation = pickupLocation;
	    }

	    public String getDropLocation() {
	        return dropLocation;
	    }

	    public void setDropLocation(String dropLocation) {
	        this.dropLocation = dropLocation;
	    }

	    public String getCarModel() {
	        return carModel;
	    }

	    public void setCarModel(String carModel) {
	        this.carModel = carModel;
	    }

	    public String getCarNumber() {
	        return carNumber;
	    }

	    public void setCarNumber(String carNumber) {
	        this.carNumber = carNumber;
	    }

	    public String getDriverContact() {
	        return driverContact;
	    }

	    public void setDriverContact(String driverContact) {
	        this.driverContact = driverContact;
	    }

	    public double getRideDistance() {
	        return rideDistance;
	    }

	    public void setRideDistance(double rideDistance) {
	        this.rideDistance = rideDistance;
	    }

	    public double getRideDuration() {
	        return rideDuration;
	    }

	    public void setRideDuration(double rideDuration) {
	        this.rideDuration = rideDuration;
	    }

	    public double getRideCost() {
	        return rideCost;
	    }

	    public void setRideCost(double rideCost) {
	        this.rideCost = rideCost;
	    }

	    public boolean isRideCompleted() {
	        return isRideCompleted;
	    }

	    public void setRideCompleted(boolean rideCompleted) {
	        isRideCompleted = rideCompleted;
	    }

	    public boolean isDriverRated() {
	        return isDriverRated;
	    }

	    public void setDriverRated(boolean driverRated) {
	        isDriverRated = driverRated;
	    }

	    public boolean isPaymentCompleted() {
	        return isPaymentCompleted;
	    }

	    public void setPaymentCompleted(boolean paymentCompleted) {
	        isPaymentCompleted = paymentCompleted;
	    }

	    public int getNumberOfPassengers() {
	        return numberOfPassengers;
	    }

	    public void setNumberOfPassengers(int numberOfPassengers) {
	        this.numberOfPassengers = numberOfPassengers;
	    }


}
